class Solution{
  public:
     // Function to find majority element in the array
    // a: input array
    // size: size of input array
    int majorityElement(int a[], int size)
    {
        if(size==1) return a[0];
        sort(a,a+size);
        int i = 1;
        while(i<size)
        {
            int cnt = 1;
            while(i<size && a[i]==a[i-1]) ++cnt,++i;
            if(cnt>(size/2)) return a[i-1];
            ++i;
        }
        return -1;
    }
};

